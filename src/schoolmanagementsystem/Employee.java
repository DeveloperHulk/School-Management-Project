    public void connect() {
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentmgmtjava", "root", "root");
            stmt = con.createStatement();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage()); //getmessage function is used to show the predifined message
        }
    }

    public void DataLoaded()
    {
        try {
            String sql = "select * from employee";
            rs = stmt.executeQuery(sql);
            
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            
            while (rs.next()) {                
               model.addRow(new Object[]{rs.getString("id"),rs.getString("firstname"),rs.getString("lastname"),rs.getString("position"),rs.getString("age"),rs.getString("gender"),rs.getString("emailid"),rs.getString("contact"),rs.getString("permanentaddress"),rs.getString("presentaddress")});
            }
            
        } catch (Exception e) {
        }
    }
