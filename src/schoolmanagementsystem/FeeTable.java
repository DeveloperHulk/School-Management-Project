    public void connect() {
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentmgmtjava", "root", "root");
            stmt = con.createStatement();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }

    public void DataLoaded() {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            String sql = "select * from fee";
            rs = stmt.executeQuery(sql);

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("id"), rs.getString("remainingfee"), rs.getString("paidfee"), rs.getString("totalfee")});
            }
        } catch (Exception e) {
        }
    }

